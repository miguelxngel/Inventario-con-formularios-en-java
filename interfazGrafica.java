    private void button_insertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_insertarActionPerformed
        int item = seleccionable.getSelectedIndex();
        switch(item){
            case 0: dulceria d1 = new dulceria();
                    d1.insertarProductoDulceria(txt_id,txt_nombreProducto, txt_cantidad);
            break;
            case 1: granos g1 = new granos();
                    g1.insertarProductoGranos(txt_id, txt_nombreProducto, txt_cantidad);
            break;
            case 2: aseo a1 = new aseo();
                    a1.insertarProductoAseo(txt_id, txt_nombreProducto, txt_cantidad);
            break;
        }
        
    }//GEN-LAST:event_button_insertarActionPerformed

